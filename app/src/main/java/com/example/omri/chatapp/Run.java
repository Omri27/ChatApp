public class Run {
    private String creator;
    private String time;
    private String date;
    private String name;
    private String location;



    public Run() {
    }

    public String getLocation() {
        return location;
    }


    public String getCreator() {
        return creator;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }

    public String getName() {
        return name;
    }
}

